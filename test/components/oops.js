// 1️⃣ Encapsulation
// ➡ Data aur methods ko ek class ke andar bundle karna.
// ➡ Isse data protected aur controlled access hota hai.
// 2️⃣ Abstraction
// ➡ Sirf zaroori information dikhana, implementation details hide karna.
// ➡ User ko simple interface milta hai, complexity background me hoti hai.
// 3️⃣ Inheritance
// ➡ Ek class ko doosri class ke properties aur methods reuse karne dena.
// ➡ Isse code duplication kam hota hai aur reusability badh jati hai.
// 4️⃣ Polymorphism
// ➡ Same function/method ka alag-alag behavior dikhana depending on 
// 
//object.
// ➡ Ye mostly method overriding ya overloading se achieve hota hai.


